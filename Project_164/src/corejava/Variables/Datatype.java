package corejava.Variables;

public class Datatype {

	public static void main(String[] args)
	{
		
		
		//Primitive datatype
		
		/*
		 * Int:--> Only accept numbers to store
		 * 		   Default value is 0
		 */
		int a=100;
		int b=200;
		int c=a+b;
		System.out.println("total value is => "+c);
		
	
		
		
		/*
		 * boolean:-->  accept only true/false
		 * 				default value is false.
		 */
		boolean flag=true;
		System.out.println("Status is => "+flag);
		
		
		
		/*
		 * double:--> 15 significant decimals it allows
		 */
		double price=1234.55;
		System.out.println("Double value is => "+price);
		
		
		//Non-primitive datatype..
		
		/*
		 * String:--> String accept set of characters to store 
		 * 			  and default value is null.
		 */
		String name="Mindq";
		String email="info@mindqsystems.com";
		String mobile="9652680806";
		
		System.out.println(name+"   "+email+"   "+mobile);
		
		
		
		

	}

}
