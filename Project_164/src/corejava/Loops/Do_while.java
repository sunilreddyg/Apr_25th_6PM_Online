package corejava.Loops;

public class Do_while 
{

	public static void main(String[] args) 
	{
		
		
		/*
		 * Do-while:-->
		 * 			=> Do-while is similar to while loop
		 * 			=> only diff it accept iteration once even condition return false.
		 */
		
		
		int i=0;
		do {
			
			i=i+1;
			System.out.println(i);
			
		} while (i < 10);
		
		
		
	}

}
